public class GraphicContextException extends Exception {

	private static final long serialVersionUID = 4027760118326279255L;

	/**
	 * 
	 */
	public GraphicContextException() {
		super();
	}

	/**
	 * @param message
	 * @param cause
	 */
	public GraphicContextException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param message
	 */
	public GraphicContextException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public GraphicContextException(Throwable cause) {
		super(cause);
	}

}
